#!/usr/bin/env node
/**
 * Build the assistant prompt pack into a bundled TS module.
 *
 * The in-app assistant cannot read the repo filesystem, so the design docs it
 * needs must be compiled into the app. We do this via codegen (not a metro
 * `.md` transformer) because `.md` is far too common to add to `sourceExts` —
 * doing so makes Metro index ~18k markdown files across the monorepo and pegs
 * the packager. This reads a fixed list of docs and emits pack.generated.ts.
 *
 * Run automatically via the `prestart` hook, or manually: `yarn pack:build`.
 */
const fs = require('fs');
const path = require('path');

const DESIGN = path.resolve(__dirname, '../../../design');
const OUT = path.resolve(__dirname, '../src/assistant/pack.generated.ts');

// Assistant operating-manual docs (prompt-pack tier).
const PACK_DOCS = {
  overview: 'specs/mobile/global/assistant/overview.md',
  protocol: 'specs/mobile/global/assistant/protocol.md',
  tools: 'specs/mobile/global/assistant/tools.md',
  guardrails: 'specs/mobile/global/assistant/guardrails.md',
  actionPlan: 'specs/mobile/global/assistant/action-plan.md',
  attachments: 'specs/mobile/global/assistant/attachments.md',
};

// Domain-truth docs, injected as named resources.
const DOMAIN_DOCS = {
  SCHEMA_QSQL: 'specs/domain/schema.qsql',
  TAXONOMY_DOC: 'specs/domain/taxonomy.md',
  BUNDLES_DOC: 'specs/domain/bundles.md',
  LOGGING_DOC: 'specs/domain/logging.md',
  IMPORT_EXPORT_DOC: 'specs/domain/import-export.md',
};

function read(rel) {
  const file = path.join(DESIGN, rel);
  return fs.readFileSync(file, 'utf8');
}

function emitObject(name, spec) {
  const entries = Object.entries(spec)
    .map(([key, rel]) => `  ${key}: ${JSON.stringify(read(rel))},`)
    .join('\n');
  return `export const ${name} = {\n${entries}\n} as const;\n`;
}

const header =
  '// AUTO-GENERATED by scripts/build-assistant-pack.js — do not edit by hand.\n' +
  '// Regenerate with `yarn pack:build` (also runs automatically on `yarn start`).\n\n';

const body = emitObject('PACK_DOCS', PACK_DOCS) + '\n' + emitObject('DOMAIN_DOCS', DOMAIN_DOCS);

fs.writeFileSync(OUT, header + body);
const bytes = Buffer.byteLength(header + body);
console.log(
  `[assistant-pack] wrote ${path.relative(process.cwd(), OUT)} ` +
    `(${Object.keys(PACK_DOCS).length + Object.keys(DOMAIN_DOCS).length} docs, ${bytes} bytes)`,
);
